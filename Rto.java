package com.Xworkz.ControlStatement;

public class Rto {
	public static void main(String[] args){
		int rtonumber=20;
		switch(rtonumber) {
		case 50 :
			System.out.println("Hunsur");
			break;
		case 45 :
			System.out.println("Banglore");
			break;
		case 10 :
			System.out.println("Chamraj nagar");
			break;
		case 20 :
			System.out.println("Mandya");
			break;
		case 30 :
			System.out.println("Tumkur");
			break;
			default : System.out.println("Wrong RTO number");
		}
	}
}
